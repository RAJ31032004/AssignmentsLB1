////////////////////////////////////////////////////////////////////////////////

#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
//
// Function Name : MultiplyInt
// Description   : Multiplies two integers
// Input         : Two integers
// Output        : Integer result
//
////////////////////////////////////////////////////////////////////////////////

int MultiplyInt(int no1, int no2)
{
    int ans = 0;
    ans = no1 * no2;
    return ans;
}

////////////////////////////////////////////////////////////////////////////////
//
// Function Name : MultiplyFloat
// Description   : Multiplies two float numbers
// Input         : Two floats
// Output        : Float result
//
////////////////////////////////////////////////////////////////////////////////

float MultiplyFloat(float no1, float no2)
{
    float ans = 0.0f;
    ans = no1 * no2;
    return ans;
}

////////////////////////////////////////////////////////////////////////////////
//
// Generic Macro
//
////////////////////////////////////////////////////////////////////////////////

#define Multiply(no1, no2) _Generic((no1), \
        int:   MultiplyInt,                \
        float: MultiplyFloat               \
    )(no1, no2)

////////////////////////////////////////////////////////////////////////////////
//
// Entry Point Function
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iRet = 0;
    float fRet = 0.0f;

    iRet = Multiply(10, 20);
    printf("%d\n", iRet);          // Output : 200

    fRet = Multiply(10.0f, 20.0f);
    printf("%f\n", fRet);          // Output : 200.000000

    return 0;
}

/*
    Time Complexity : O(1)
*/
