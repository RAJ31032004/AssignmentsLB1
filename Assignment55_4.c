#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name :   SwapInt
//  Description   :   Swaps two integer values
//  Input         :   Address of two integers
//  Output        :   Values swapped
//
/////////////////////////////////////////////////////////////////////////////////////////////
void SwapInt(int *no1, int *no2)
{
    int temp = *no1;
    *no1 = *no2;
    *no2 = temp;
}

/////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name :   SwapFloat
//  Description   :   Swaps two float values
//  Input         :   Address of two floats
//  Output        :   Values swapped
//
/////////////////////////////////////////////////////////////////////////////////////////////
void SwapFloat(float *no1, float *no2)
{
    float temp = *no1;
    *no1 = *no2;
    *no2 = temp;
}

/////////////////////////////////////////////////////////////////////////////////////////////
//
//  Generic Macro :   Swap
//  Description   :   Selects correct swap function based on datatype
//
/////////////////////////////////////////////////////////////////////////////////////////////
#define Swap(no1, no2) _Generic((no1), \
                        int   : SwapInt, \
                        float : SwapFloat \
                      )(&(no1), &(no2))

/////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//
/////////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    int a = 10, b = 20;
    Swap(a, b);
    printf("After swap int values : %d %d\n", a, b);

    float x = 3.5f, y = 7.8f;
    Swap(x, y);
    printf("After swap float values : %.2f %.2f\n", x, y);

    return 0;
}
