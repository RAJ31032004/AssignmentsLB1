#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name :   SubInt
//  Description   :   Performs subtraction of two integers
//  Input         :   Integer, Integer
//  Output        :   Integer
//
/////////////////////////////////////////////////////////////////////////////////////////////
int SubInt(int no1, int no2)
{
    return no1 - no2;
}

/////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name :   SubFloat
//  Description   :   Performs subtraction of two float numbers
//  Input         :   Float, Float
//  Output        :   Float
//
/////////////////////////////////////////////////////////////////////////////////////////////
float SubFloat(float no1, float no2)
{
    return no1 - no2;
}

/////////////////////////////////////////////////////////////////////////////////////////////
//
//  Generic Macro :   Sub
//  Description   :   Selects correct function based on datatype
//
/////////////////////////////////////////////////////////////////////////////////////////////
#define Sub(no1, no2) _Generic((no1), \
                        int   : SubInt, \
                        float : SubFloat \
                      )(no1, no2)

/////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//
/////////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    int iRet = Sub(20, 10);
    printf("%d\n", iRet);

    float fRet = Sub(20.5f, 10.3f);
    printf("%f\n", fRet);

    return 0;
}
